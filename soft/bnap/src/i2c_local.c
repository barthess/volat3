#include "ch.h"
#include "hal.h"

#include "main.h"
#include "i2c_local.h"

/*
 ******************************************************************************
 * DEFINES
 ******************************************************************************
 */
#define CLDIV 16
#define CHDIV 9
#define CKDIV 3
#define CWGR  ((CKDIV << 16) | (CHDIV << 8) | (CLDIV << 0))

/*
 ******************************************************************************
 * EXTERNS
 ******************************************************************************
 */

extern GlobalFlags_t GlobalFlags;

/*
 ******************************************************************************
 * GLOBAL VARIABLES
 ******************************************************************************
 */
static const I2CConfig i2ccfg = {CWGR};

/*
 *******************************************************************************
 *******************************************************************************
 * LOCAL FUNCTIONS
 *******************************************************************************
 *******************************************************************************
 */

void i2cLocalInit(void) {
  i2cStart(&I2CD1, &i2ccfg);
  setGlobalFlag(GlobalFlags.i2c_ready);
}
